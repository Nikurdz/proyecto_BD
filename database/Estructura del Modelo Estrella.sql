-- 1. Tablas de Dimensiones
CREATE TABLE DIM_CLIENTE (
   CLI_ID VARCHAR2(13) PRIMARY KEY,
   CLI_NOMBRE VARCHAR2(60),
   CLI_DIRECCION VARCHAR2(60),
   CLI_TELEFONO VARCHAR2(10),
   CLI_CORREO VARCHAR2(60)
) TABLESPACE DATA;

CREATE TABLE DIM_PRODUCTO (
   PRD_ID VARCHAR2(10) PRIMARY KEY,
   PRD_NOMBRE VARCHAR2(30),
   PRD_PRECIO NUMBER(12,2)
) TABLESPACE DATA;

CREATE TABLE DIM_TIEMPO (
   FECHA DATE PRIMARY KEY,
   DIA NUMBER,
   MES NUMBER,
   ANIO NUMBER,
   DIA_SEMANA VARCHAR2(20)
) TABLESPACE DATA;

-- 2. Tabla de Hechos
CREATE TABLE HECHOS_PEDIDOS (
   HECHO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   CLI_ID VARCHAR2(13),
   PED_ID NUMBER(12),
   PRD_ID VARCHAR2(10),
   PPC_CANTIDAD NUMBER(12,2),
   PAG_MONTO NUMBER(12,2),
   FECHA DATE,
   FOREIGN KEY (CLI_ID) REFERENCES DIM_CLIENTE(CLI_ID),
   FOREIGN KEY (PRD_ID) REFERENCES DIM_PRODUCTO(PRD_ID),
   FOREIGN KEY (FECHA) REFERENCES DIM_TIEMPO(FECHA)
) TABLESPACE DATA;